/**
 * Definition for singly-linked list.
 * function ListNode(val) {
 *     this.val = val;
 *     this.next = null;
 * }
 */

/**
 * @param {ListNode} head
 * @return {boolean}
 */
var hasCycle = function (head) {
  var current = head
  while (current) {
    if (current.mark) {
      return true
    }
    current.mark = true
    current = current.next
  }
  return false
};
